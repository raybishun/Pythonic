# -----------------------------------------------------------------------------
# Numeric Operators
# -----------------------------------------------------------------------------
i = 1
j = 2.0
print(i)
print(j)
print(type(i))
print(type(j))
print()

# -----------------------------------------------------------------------------
# Numeric Operators: Addition
# -----------------------------------------------------------------------------
# int + float, returns a float
print( 1 + 2.0 )
print(type(1 + 2.0))
print()

# -----------------------------------------------------------------------------
# Numeric Operators: Multiplication
# -----------------------------------------------------------------------------
print( 2 * 3 )
print()

# -----------------------------------------------------------------------------
# Numeric Operators: Standard Division
# -----------------------------------------------------------------------------
print( 5 / 2 )      # 2.5 (ALWAYS returns a float)
print( type(5 / 2) ) 
print()

# -----------------------------------------------------------------------------
# Numeric Operators: Floor Division (return the whole number as an int)
# -----------------------------------------------------------------------------
print( 5 // 2 )     # 2
print( type(5 // 2) )
print()

# -----------------------------------------------------------------------------
# Numeric Operators: Modulo (returns the remainder as an int, and rounds up)
# -----------------------------------------------------------------------------
print( 5 % 2 )      # returns 1 not .5 (as an int)
print( type(5 % 2) )
print()

# -----------------------------------------------------------------------------
# Numeric Operators: Exponents
# -----------------------------------------------------------------------------
print( 2 ** 3 )     # 2 x 2 x 2 (2 to the power of 3)
print( type(2 ** 3) )
print()