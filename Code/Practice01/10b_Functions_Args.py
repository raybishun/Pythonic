# -----------------------------------------------------------------------------
# Functions: *args and **kwargs
# -----------------------------------------------------------------------------

# *args
# -----------------------------------------------------------------------------
def add(*args):
    return sum(args)

print(add(2,3,3,2))

# **kwargs
# -----------------------------------------------------------------------------
def get_user_data(**kwargs):
    return(kwargs)

# Returns a dictionary
print(get_user_data(Name='Ray', UPN='ray@email.com'))