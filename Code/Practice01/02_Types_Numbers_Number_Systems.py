# -----------------------------------------------------------------------------
# Number Systems
# -----------------------------------------------------------------------------
# Decimal       -   Base 10     -   0 thru 9                Prefix: n/a
# Binary        -   Base 2      -   0 or 1                  Prefix: 0b
# Octal         -   Base 8      -   0 thru 7                Prefix: 0o
# Hexadecimal   -   Base 16     -   0 thru 9, and A thru F  Prefix: 0x


# Decimal
print(15)

# Binary
print(0b1111)

# Octal (zero with a lower-case o)
print(0o17)

# Hexadecimal
print(0xf)


