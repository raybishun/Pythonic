# -----------------------------------------------------------------------------
# Types: Integers, Floats and Scientific Notation
# -----------------------------------------------------------------------------

# Integers
i = 1
print(i)
print(type(i))
print(id(i))
print()

# Floats
i = 1.0
print(i)
print(type(i))
print(id(i))
print()

# -----------------------------------------------------------------------------
# Scientific Notation
# -----------------------------------------------------------------------------
i = 2e3             # 'e' means 'exponent'
j = 2 * (10 ** 3)   # 10 to the power of 3
print(i)
print(j)
print(type(i))
print(type(j))
print(id(i))
print(id(j))
print(i == j)
print()